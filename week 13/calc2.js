function calcPlus() {
  let numberOne = prompt("Введите первое число", 1);
  let numberTwo = prompt("Введите второе число", 1);
  let resultPlus = +numberOne + +numberTwo;
  alert(resultPlus);
}

function calcMinus() {
  let numberOne = prompt("Введите первое число", 1);
  let numberTwo = prompt("Введите второе число", 1);
  let resultMinus = numberOne - numberTwo;
  alert(resultMinus);
}

function calcDivision() {
  let numberOne = prompt("Введите первое число", 1);
  let numberTwo = prompt("Введите второе число", 1);
  let resultDivision = numberOne / numberTwo;
  alert(resultDivision);
}

